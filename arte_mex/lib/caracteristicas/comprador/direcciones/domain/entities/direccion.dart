class Direccion {
  final String estado;
  final String codigoPostal;
  final String municipio;
  final String calle;
  final String numExterior;
  final String numInterior;
  final String numeroTelf;
  final String descripcionDomicilio;

  Direccion({
    required this.estado,
    required this.codigoPostal,
    required this.municipio,
    required this.calle,
    required this.numExterior,
    required this.numInterior,
    required this.numeroTelf,
    required this.descripcionDomicilio,
  });
}
